            UpdateGenderControls();
            UpdateSkinColor();
            UpdateSpawnPriorityControls();
            UpdateJobSubnameControls(); // WL-subnames: Pupchansky
            UpdateHeightEdit(); // WL-height
            UpdateOocTextEdit(); // WL-OocText
            UpdateRecordsEdit(); // WL-Records
            UpdateAgeEdit();
