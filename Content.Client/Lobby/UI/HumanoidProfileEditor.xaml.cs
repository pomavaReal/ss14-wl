    public sealed partial class SubnameSelector : Control
    {
        public readonly JobPrototype Job;

        public Action<int, string, bool>? SubnameChanged;

        private readonly List<string> _subnames;

        public readonly OptionButton? Options;

        // WL-changes start
        public SubnameSelector(JobPrototype job, Gender gender)
        {
            Job = job;

            var subnames = new List<string>()
            {
                job.LocalizedName
            };
            subnames.AddRange(job.GetSubnames(gender).Order());

            _subnames = subnames;

            if (_subnames.Count > 1)
            {
                Options = new() { HorizontalAlignment = HAlignment.Left };

                var indexer = 0;
                foreach (var subname in subnames)
                {
                    Options.AddItem(subname, indexer);
                    indexer += 1;
                }

                Options.OnItemSelected += (args) =>
                {
                    SelectItem(args.Id, false);
                };

                AddChild(Options);
            }
            else AddChild(new Label() { Text = _subnames.First() });
        }
        // WL-changes End

        public void SelectItem(int index, bool silent)
        {
            if (Options == null)
                return;

            Options.SelectId(index);

            SubnameChanged?.Invoke(index, _subnames[index], silent);
        }

        public void SelectItem(string subname, bool silent)
        {
            var index = _subnames.IndexOf(subname);

            SelectItem(index, silent);
        }
    }


